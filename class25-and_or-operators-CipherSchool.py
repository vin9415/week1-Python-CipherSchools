## check two conditions at same time 
## and  , or
name = 'abc'
age = 19
if name=='abc' and age==19:
    print("Condition True")
else:
    print("Condition False")    
''' in "and" opeerators ,we have to see both the condition should be true ,if not then this condition will not call "and" '''


## or operator  ---> if one condtion is true , then our overall condition would be true 

name = 'abc'
age = 19
if name=='abcd' or age==19:
    print("Condition True")
else:
    print("condition False")


